public class Complaints {

    private int employeeNum;
    private int complaintNumber;
    private String ComplaintLink;

    public int getEmployeeNum() {
        return employeeNum;
    }

    public void setEmployeeNum(int employeeNum) {
        this.employeeNum = employeeNum;
    }

    public int getComplaintNumber() {
        return complaintNumber;
    }

    public void setComplaintNumber(int complaintNumber) {
        this.complaintNumber = complaintNumber;
    }

    public String getComplaintLink() {
        return ComplaintLink;
    }

    public void setComplaintLink(String complaintLink) {
        ComplaintLink = complaintLink;
    }
}
